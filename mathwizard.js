function add(num1, num2) {
  return num1 + num2;
}
console.log(add(1, 2));

function multiply(num1, num2) {
  return num1 * num2;
}
console.log(multiply(3, 3));

function subtract(num1, num2) {
  return num1 - num2;
}
console.log(subtract(10, 5));

function divide(num1, num2) {
  return num1 / num2;
}
console.log(divide(100, 10));
